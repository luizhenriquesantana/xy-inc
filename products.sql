CREATE TABLE PRODUCT 
(
  P_ID NUMBER NOT NULL 
, P_NAME VARCHAR2 (100) NOT NULL 
, P_DESCRIPTION VARCHAR2 (4000) NOT NULL
, P_PRICE NUMBER(8,2) NOT NULL
, P_CATEGORY VARCHAR2 (100) NOT NULL
, CONSTRAINT PK_PRODUCT PRIMARY KEY 
  (
    P_ID 
  )
  ENABLE 
);


CREATE SEQUENCE "SQ_PRODUCT" MINVALUE 1 MAXVALUE 999999999999999999999999999 INCREMENT BY 1 START WITH 1 NOCACHE NOORDER NOCYCLE;


